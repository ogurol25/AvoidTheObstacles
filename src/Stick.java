public class Stick {
    public String name;
    public int xpos;
    public int ypos;
    public int width;
    public int height;

    public Stick(String pName, int pXpos, int pYpos, int width2, int height2){
        name = pName;
        xpos = pXpos;
        ypos = pYpos;
        width = width2;
        height = height2;
    }

}